    function applyLang(lang){
      document.querySelectorAll('[data-key]').forEach(el => {
        const key = el.getAttribute('data-key');
        if(translations[lang] && translations[lang][key] !== undefined){
          const text = translations[lang][key];
          if (key === 'contact_text') {
            const parts = text.split('\n');
            el.innerHTML = `
              <span style="display:flex; align-items:center; gap:8px; margin-bottom: 5px;">
                <span class="material-icons" style="font-size: 18px; color: var(--primary);">phone</span> 
                ${parts[0]}
              </span>
              <span style="display:flex; align-items:center; gap:8px;">
                <span class="material-icons" style="font-size: 18px; color: var(--primary);">email</span> 
                ${parts[1]}
              </span>
            `;
          } else {
            el.innerText = text;
          }
        }
      });

      document.querySelectorAll('.langbtn').forEach(b=>{
        b.classList.toggle('active', b.getAttribute('data-lang') === lang);
      });

      localStorage.setItem('adama_lang', lang);
    }

    document.addEventListener('DOMContentLoaded', ()=>{
      document.querySelectorAll('.langbtn').forEach(b=>{
        b.addEventListener('click', ()=>applyLang(b.getAttribute('data-lang')));
      });

      document.querySelectorAll('.faq-item strong').forEach(item => {
        item.addEventListener('click', () => {
          item.parentNode.classList.toggle('active');
        });
      });

      const videoElement = document.getElementById('heroVideo');
      const videoSources = [
        '/videos/video1.mp4', // Corrected path assumption
        '/videos/video2.mp4',
        '/videos/video3.mp4'
      ];
      let currentVideoIndex = 0;

      function playNextVideo() {
        if (videoSources.length === 0) return; // Handle no videos
        videoElement.src = videoSources[currentVideoIndex];
        videoElement.load();
        videoElement.play().catch(error => console.error("Video playback failed:", error)); // Catch play errors
        currentVideoIndex = (currentVideoIndex + 1) % videoSources.length;
      }

      videoElement.addEventListener('ended', playNextVideo);

      if (videoSources.length > 0) {
        playNextVideo();
      }

      const saved = localStorage.getItem('adama_lang') || 'en';
      applyLang(saved);

      // Handle active navigation link
      const path = window.location.pathname;
      document.querySelectorAll('.header-nav .nav-link').forEach(link => {
          if (link.getAttribute('href') === path) {
              link.classList.add('active');
          } else {
              link.classList.remove('active');
          }
      });
    });

